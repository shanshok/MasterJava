package javaashok;

public class PrintlnVSprint {

	public static void main(String[] args) {
		
		/* This program is to print the given value next to each other than below each other using SOP as print than as SOP as println*/		
		int i=1,  n=3;
		
		while (i<n)
		{
			System.out.print("*"); /* This is print than println */
			i++;
			
		}
	}

}