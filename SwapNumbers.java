package javaashok;

public class SwapNumbers {

	public static void main(String[] args) {
		
		/* This program is to swap number stored in a to b and from b to a*/
	
		int a=5;
		int b=10;
		/* create a temporary variable and swap b value to C*/
		int c=b;
		/* Now swap a value to d*/
		int d=a;
		
        System.out.println(c);
        System.out.println(d);

	}

}
